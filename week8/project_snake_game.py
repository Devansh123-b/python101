# 🐍 PROJECT: Snake Game in Python Turtle
# ─────────────────────────────────────────────────────────────────

# Project Description:
# In this project, students will create a basic version of the classic
# Snake game using Python and the Turtle graphics library.
# The goal is to learn how to control objects on the screen,
# respond to keyboard input, and build a simple game loop.

# The player controls a snake using the arrow keys.
# The snake moves around the screen and tries to eat food
# that appears randomly. Each time the snake eats the food,
# the score increases. If the snake hits the wall,
# the game resets.

# Learning Objectives:
# - Understand how to use the Turtle module for graphics
# - Practice using variables, functions, and loops
# - Learn how to handle keyboard input
# - Use conditionals (if statements) for game logic
# - Work with coordinates and movement on a screen

# How the Game Works:
# 1. The snake starts in the center of the screen.
# 2. The player uses arrow keys to move the snake
#    up, down, left, or right.
# 3. Food appears at random positions.
# 4. When the snake touches the food:
#    - The score increases
#    - The food moves to a new random location
# 5. If the snake hits the wall:
#    - The game resets
#    - The score goes back to zero

# Possible Extensions (Optional Challenges):
# - Make the snake grow longer when it eats food
# - Add sound effects or background music
# - Increase the speed as the score increases
# - Add a "Game Over" message
