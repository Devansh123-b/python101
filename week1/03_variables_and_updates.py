# Python101 - Lesson 3: Variables + Updating (state changes)

# Variables store information
name = "Aisha"
score = 10

print("Player:", name)
print("Score:", score)

print()  # blank line

# Updating a variable (very common move!)
print("Start:", score)

score = score + 5
print("After bonus:", score)

score = score - 3
print("After penalty:", score)

# Teaching notes:
# - name and score are "labels" that point to values
# - score = score + 5 means: take the old score, add 5, store it back
