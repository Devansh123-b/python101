# Python101 - Lesson 2: Strings vs Numbers (the classic "type surprise")

print("Numbers do math:")
print(5 + 5)      # 10

print()  # blank line

print("Strings glue together (concatenate):")
print("5" + "5")  # "55" (text)

print()  # blank line

# Another tiny example:
print("Score:" + " " + "10")  # still text

# Teaching notes:
# - Quotes (" ") create a string (text)
# - 5 is a number; "5" is text that looks like a number
